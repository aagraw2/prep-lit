package com.preplit.model;

/**
 * Anti-patterns detected during the interview.
 */
public enum AntiPattern {
    /**
     * Candidate jumped to coding without clarifying requirements.
     */
    JUMPED_TO_CODE,
    
    /**
     * Candidate didn't mention or consider edge cases.
     */
    NO_EDGE_CASES,
    
    /**
     * Candidate asked for the answer directly.
     */
    ASKED_FOR_ANSWER,
    
    /**
     * Candidate asked zero clarifying questions.
     */
    NO_CLARIFICATION
}
